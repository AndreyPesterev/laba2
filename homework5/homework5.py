def get_number():
    # Генератор для возвращения нечетных чисел в диапазоне range(30)
    for num in range(30):
        if num % 2 != 0:  # Проверка на нечетность
            yield num

def main():
    # Запускаем генератор и сохраняем его значения в список
    odd_numbers = list(get_number())

    # Проверка, что в списке достаточно значений для вывода
    if len(odd_numbers) < 5:
        print("Недостаточно нечетных чисел в диапазоне для выполнения задачи.")
        return

    # Вывод первого, пятого и последнего значения
    print(f"Первое нечетное число: {odd_numbers[0]}")
    print(f"Пятое нечетное число: {odd_numbers[4]}")
    print(f"Последнее нечетное число: {odd_numbers[-1]}")

if __name__ == "__main__":
    main()
